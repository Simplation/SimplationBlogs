void main() {
  int a = 10;
  int b = 5;

  // ??= 如果该符号前边的变量没有被赋值，那么该变量值为等号右边的值；如果已经赋值，则该符号不会对变量重新赋值。
  b ??= 10;
  print(b);

  a += 2;
  print("+= : $a");

  a -= b;
  //a -= 5;
  print("-= ：$a");

  a *= b;
  print("*= : $a");

  // a /= b; 返回结果为 double 类型

  a ~/= b;
  print("~/= : $a");

  a %= b;
  print("%= ：$a"); 
}