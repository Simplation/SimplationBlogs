void main() {
  var a = 10;
  print("a 的值为: $a");

  a = 50;
  print("a 的值修改为: $a");

  const b = 20;
  print("b 的值为: $b");

  final c = 30;
  //c = 40;
  print("c 的值为: $c");

  // const 和 final 使用两个生命的变量都是不能修改的, 但是有本质上的区别.
  
  
  


}

